PROCEDIMIENTOS DE ACCESO, ACTUALIZACIÓN, SUPRESIÓN Y
RECTIFICACIÓN DE DATOS PERSONALES Y DE REVOCATORIA DE LA
AUTORIZACIÓN:
La Universidad Católica de Manizales garantiza la inclusión en el Sistema
Integrado de Gestión de los diversos procedimientos que reglamentan los
usos de la información que se da en cada instancia de la Universidad.
La Secretaría General es la dependencia encargada de garantizar a los
titulares la atención oportuna a las solicitudes de acceso, actualización,
supresión o rectificación de sus datos personales.
Estas solicitudes o reclamaciones podrán ser recibidas en medio físico o
electrónico en el correo secgeneral@ucm.edu.co o a través del sistema de
PQRSF (Peticiones, Quejas, Reclamos, Sugerencias o Felicitaciones)
dispuesto para tal fin en la página web de la Universidad, serán respondidas
en un plazo no mayor a 15 días hábiles y deberán contener como mínimo la
siguiente información:
1. El nombre y domicilio del titular o cualquier otro medio para recibir la
respuesta.
2. Los documentos que acrediten la identidad o la personalidad de su
representante.
3. La descripción clara y precisa de los datos personales respecto de los
cuales el titular busca ejercer alguno de los derechos.
4. En caso dado otros elementos o documentos que faciliten la
localización de los datos personales.
5. La descripción clara de los hechos y la solicitud o reclamo que se
eleva a la Universidad.
El titular podrá presentar cualquier tipo de reclamación teniendo en cuenta
la información contenida en el Art. 15 de la Ley 1581 de 2012. 
14
Cuando la solicitud sea formulada por persona distinta del titular, deberá
acreditarse en debida forma la personería o mandato para actuar; y en caso de
no acreditar tal calidad, la solicitud se tendrá por no presentada.
Consultas. Los Titulares o sus causahabientes podrán consultar la información
personal del Titular que repose en cualquier base de datos de la Universidad
quien suministrará a estos toda la información contenida en el registro
individual o que esté vinculada con la identificación del Titular.
La consulta será atendida en un término máximo de diez (10) días hábiles
contados a partir de la fecha de recibo de la misma. Cuando no fuere
posible atender la consulta dentro de dicho término, se informará al
interesado, expresando los motivos de la demora y señalando la fecha en
que se atenderá su consulta, la cual en ningún caso podrá superar los cinco
(5) días hábiles siguientes al vencimiento del primer término.
Reclamos. Si el reclamo resulta incompleto, se requerirá al interesado dentro
de los cinco (5) días siguientes a la recepción del reclamo para que subsane
las fallas. Transcurridos dos (2) meses desde la fecha del requerimiento, sin
que el solicitante presente la información requerida, se entenderá que ha
