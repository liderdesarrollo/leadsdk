PRINCIPIOS PARA EL TRATAMIENTO DE DATOS PERSONALES.
La Universidad Católica de Manizales en atención a lo establecido en la ley
1581 de 2012 aplicará los siguientes principios específicos que se establecen
a continuación, los cuales constituyen las reglas a seguir en el tratamiento
de datos personales, es decir, en su proceso de recolección, uso,
almacenamiento, transferencia y disposición de datos personales:
a) Principio de legalidad en materia de Tratamiento de datos. El tratamiento
de datos personales es una actividad reglada que debe sujetarse a lo
establecido en la Constitución Política de Colombia, la ley, en el presente
Manual y en las demás disposiciones que complementen o desarrollen.
b) Principio de finalidad: El tratamiento de datos personales debe obedecer
a una finalidad legítima de acuerdo con la Constitución y la Ley, la cual
debe ser informada al TITULAR.
c) Principio de libertad: El tratamiento de datos personales sólo puede
ejercerse con el consentimiento, previo, expreso e informado del TITULAR.
Los datos personales no podrán ser obtenidos o divulgados sin previa
autorización, o en ausencia de mandato legal o judicial que releve el
consentimiento;
d) Principio de veracidad o calidad: La información sujeta a uso, captura,
recolección y tratamiento debe ser veraz, completa, exacta, actualizada,
comprobable y comprensible. Se prohíbe el Tratamiento de datos parciales,
incompletos, fraccionados o que induzcan a error;
e) Principio de transparencia: En el Tratamiento debe garantizarse el
derecho del TITULAR a obtener del Responsable del Tratamiento o del
Encargado del Tratamiento, en cualquier momento y sin restricciones,
información acerca de la existencia de datos que le conciernan;
f) Principio de acceso y circulación restringida: El tratamiento se sujeta a los
límites que se derivan de la naturaleza de los datos personales, de las
disposiciones de ley vigente y la Constitución. En este sentido, el Tratamiento
sólo podrá hacerse por personas autorizadas por el Titular y/o por las
personas previstas en la ley 1581 de 2012;
Los datos personales, salvo la información pública, no podrán estar
disponibles en Internet u otros medios de divulgación o comunicación
masiva, salvo que el acceso sea técnicamente controlable para brindar un 
7
conocimiento restringido sólo a los Titulares o terceros autorizados conforme
a la ley;
g) Principio de seguridad: La información sujeta a uso, captura, recolección
y tratamiento por el Responsable del Tratamiento o Encargado del
Tratamiento a que se refiere la ley, se deberá manejar con las medidas
técnicas, humanas y administrativas que sean necesarias para otorgar
seguridad a los registros evitando su adulteración, pérdida, consulta, uso o
acceso no autorizado o fraudulento;
h) Principio de confidencialidad: Todas las personas que intervengan en el
uso, captura, recolección y tratamiento de datos personales que no tengan
la naturaleza de públicos están obligadas a garantizar la reserva de la
información, inclusive después de finalizada su relación con alguna de las
labores que comprende el Tratamiento, pudiendo sólo realizar suministro o
comunicación de datos personales cuando ello corresponda al desarrollo
de las actividades autorizadas en la presente ley y en los términos de la
misma.
DERECHOS DE LOS TITULARES DE DATOS PERSONALES EN LA UNIVERSIDAD
CATÓLICA DE MANIZALES.
La Universidad Católica de Manizales garantiza los derechos de los titulares
de datos personales contenidos en el Art. 8 de la Ley 1581 de 2012, a saber:
1. Conocer, actualizar y rectificar sus datos personales frente a los
Responsables del Tratamiento o Encargados del Tratamiento. Este
derecho se podrá ejercer, entre otros frente a datos parciales,
inexactos, incompletos, fraccionados, que induzcan a error, o
aquellos cuyo Tratamiento esté expresamente prohibido o no haya
sido autorizado;
2. Solicitar prueba de la autorización otorgada al Responsable del
Tratamiento salvo cuando expresamente se exceptúe como requisito
para el Tratamiento, de conformidad con lo previsto en el artículo 10
de la presente ley;
3. Ser informado por el Responsable del Tratamiento o el Encargado del
Tratamiento, previa solicitud, respecto del uso que le ha dado a sus
datos personales;
4. Presentar ante la Superintendencia de Industria y Comercio quejas por
infracciones a lo dispuesto en la presente ley y las demás normas que
la modifiquen, adicionen o complementen;
5. Revocar la autorización y/o solicitar la supresión del dato cuando en
el Tratamiento no se respeten los principios, derechos y garantías
constitucionales y legales. La revocatoria y/o supresión procederá 
8
cuando la Superintendencia de Industria y Comercio haya
determinado que en el Tratamiento el Responsable o Encargado han
incurrido en conductas contrarias a esta ley y a la Constitución;
6. Acceder en forma gratuita a sus datos personales que hayan sido
objeto de Tratamiento.
Legitimación para el ejercicio de los derechos del titular. De conformidad
con el Art. 20 del Decreto 1377 de 2013, los derechos de los Titulares
establecidos en la Ley, podrán ejercerse por las siguientes personas:
1. Por el Titular, quien deberá acreditar su identidad en forma suficiente por
los distintos medios que le ponga a disposición el responsable.
2. Por sus causahabientes, quienes deberán acreditar tal calidad.
3. Por el representante y/o apoderado del Titular, previa acreditación de la
representación o apoderamiento.
4. Por estipulación a favor de otro o para otro.
Los derechos de los niños, niñas o adolescentes se ejercerán por las personas
que estén facultadas para representarlos.
Personas a quienes se les puede suministrar la información. La información
que reúna las condiciones establecidas en la presente ley podrá
suministrarse a las siguientes personas:
a) A los Titulares, sus causahabientes o sus representantes legales;
b) A las entidades públicas o administrativas en ejercicio de sus funciones
legales o por orden judicial;
c) A los terceros autorizados por el Titular o por la ley.
