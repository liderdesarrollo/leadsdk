MARCO LEGAL Y CONSTITUCIONAL.
La Universidad Católica de Manizales se acoge a lo establecido en los Art.
15 de la Constitución Política de Colombia, derechos al buen nombre,
habeas data, intimidad personal y familiar, así como el derecho a solicitar
actualización y rectificación respecto a los datos personales. Se integran al
presente Manual las leyes estatutarias 1266 de 2008 y 1581 de 2012 que
desarrollaron dichos derechos constitucionales que fueron a su vez
reglamentados por los decretos 1727 de 2009, 2952 de 2010 y 1377 de 2013.
En concordancia con el principio de Autonomía Universitaria establecido en
los Art. 67, 68 y 69 de la Constitución Política de Colombia se interpretarán
los principios de libertad, legalidad, veracidad o calidad de datos, finalidad,
circulación restringida, temporalidad, interpretación integral, seguridad,
transparencia y confidencialidad de acuerdo con el derecho a la
educación, su función social y reglamentación.
El tratamiento de la información personal se adelantará de acuerdo con los
principios antes indicados y en el marco de lo dispuesto por la Ley 30 de
1992, los decretos reglamentarios 1767 de 2006 y 4968 de 2009 y la Resolución
1780 del Ministerio de Educación Nacional.
La Universidad Católica de Manizales integra a su normativa las definiciones
legales contenidas en la Ley 1581 de 2012 y decreto 1377 de 2013 sobre los
siguientes conceptos: autorización, bases de datos, dato personal,
encargado del tratamiento, responsable del tratamiento, titular de la
información, fuente de información, operador de información, tratamiento,
usuario, dato público, dato semiprivado, dato privado, transmisión,
transferencia, dato sensible.
