AUTORIZACIÓN.
La Universidad Católica de Manizales garantiza la adopción de
procedimientos para solicitar al titular la autorización previa, expresa, libre e
informada, la cual puede ser obtenida por medio físico o electrónico que
permita su consulta y verificación posterior.
Casos en que no es necesaria la autorización. La autorización del Titular no
será necesaria cuando se trate de:
a) Información requerida por una entidad pública o administrativa en
ejercicio de sus funciones legales o por orden judicial;
b) Datos de naturaleza pública;
c) Casos de urgencia médica o sanitaria;
d) Tratamiento de información autorizado por la ley para fines históricos,
estadísticos o científicos;
e) Datos relacionados con el Registro Civil de las Personas.
Quien acceda a los datos personales sin que medie autorización previa
deberá en todo caso cumplir con las disposiciones contenidas en la
presente ley.
